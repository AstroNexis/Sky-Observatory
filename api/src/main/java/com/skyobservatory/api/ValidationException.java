package com.skyobservatory.api;

/**
 * Thrown when an input value fails a domain validation rule.
 *
 * Callers that need to distinguish an invalid input from a calculation failure
 * can catch this specific subtype. Callers that do not care about the distinction
 * can catch the base {@link AstroException}.
 */
public final class ValidationException extends AstroException {

    public ValidationException(String message) {
        super(message);
    }

    public ValidationException(String message, Throwable cause) {
        super(message, cause);
    }
}
